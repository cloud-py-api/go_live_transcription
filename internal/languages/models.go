package languages

type LanguageMetadata struct {
	Separator string `json:"separator"`
	RTL       bool   `json:"rtl"`
}

type LanguageModel struct {
	Name     string           `json:"name"`
	Metadata LanguageMetadata `json:"metadata"`
}

func newLang(name string) LanguageModel {
	return LanguageModel{Name: name, Metadata: LanguageMetadata{Separator: " "}}
}

func newLangRTL(name string) LanguageModel {
	return LanguageModel{Name: name, Metadata: LanguageMetadata{Separator: " ", RTL: true}}
}

func newLangNoSep(name string) LanguageModel {
	return LanguageModel{Name: name, Metadata: LanguageMetadata{Separator: ""}}
}

